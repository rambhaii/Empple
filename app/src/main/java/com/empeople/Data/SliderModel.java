package com.empeople.Data;

public class SliderModel {
    int img;
    int image;
    public SliderModel (int img,int image)
    {
        this.img=img;
        this.image=image;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
    public int getImage (int image)
    {
        return image;
    }
    public void setImage(int image)
    {
        this.image=image;
    }

}
